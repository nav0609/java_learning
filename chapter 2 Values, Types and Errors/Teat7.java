//program to assign fractional no. and find their square roots
class Teat7
{
    static void main()
    {
        double n1, n2, n3;
        n1 = 16.0;
        n2 = 25.0;
        n3 = 36.0;
        double sq1, sq2, sq3;
        sq1 = Math.sqrt(n1); 
        sq2 = Math.sqrt(n2); 
        sq3 = Math.sqrt(n3); 
        System.out.println("The Square Root of the first fraction ="+ sq1);
        System.out.println("The Square Root of the second fraction ="+ sq2);
        System.out.println("The Square Root of the third fraction ="+ sq3);
    }
}