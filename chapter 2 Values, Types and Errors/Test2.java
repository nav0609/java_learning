//program to find the sum and products of three decimals
class Test2
{
    static void main()
    {
        double n1, n2, n3;
        n1 = 2.5;
        n2 = 3.5;
        n3 = 4.5;
        double sum, product;
        sum = n1 + n2 + n3;
        product = n1*n2*n3;
        System.out.println("The sum of the three decimals is ="+ sum);
        System.out.println("The product of the three decimals is ="+ product);
    }
}
        