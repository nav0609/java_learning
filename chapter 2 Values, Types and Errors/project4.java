//program to find average and cube 
public class project4
{
    public static void main()
    {
        int n1, n2, n3, avg;
        double y, cube;
        n1 = 5;
        n2 = 10;
        n3 = 15;
        y = 20.5;
        avg = ( n1 + n2 + n3 ) / 3;
        cube = y * y * y;
        
        System.out.println("Average of 5, 10, 15 is = " + avg );
        System.out.println("Cube of 20.5 is = " +cube );
    }
}
    